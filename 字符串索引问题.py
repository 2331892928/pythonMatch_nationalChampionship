# 5. 点击右上方“结束任务”按钮，系统将在后台计算任务得分，任务结束。
class Solution:

    def str_prc(self, s: str, c: str) -> int:
        n = s.find(c)
        m = n ** 3
        return m % 11


S = Solution()
print(S.str_prc("TIzFVZvth", "F"))
